package h3cCrypto

import (
	"encoding/hex"
	"reflect"
	"testing"
)

func TestChallangeResponse(t *testing.T) {
	challangeRequest := []byte{0xcf, 0xfe, 0x64, 0x73, 0xd5, 0x73, 0x3b, 0x1f, 0x9e, 0x9a, 0xee, 0x1a, 0x6b, 0x76, 0x47, 0xc8, 0x9e, 0x27, 0xc8, 0x92, 0x25, 0x78, 0xc4, 0xc8, 0x27, 0x03, 0x34, 0x50, 0xb6, 0x10, 0xb8, 0x35}
	got := ChallangeResponse(challangeRequest, H3C_INFO)
	want, _ := hex.DecodeString("8719362833108a6e16b08e33943601542511372d8d1fb1ab31aa17059118a6ba")
	if !reflect.DeepEqual(got, want) {
		t.Errorf("got %q want %q", got, want)
	}
}
